
public class forLoop {

	public static void main(String[] args) {
		// for loop executes code a limited amount of times
		//i is short for index
		
		for(int i = 1; i <=10; i++ ) {
			System.out.print(i + " ");
		}
		System.out.println();
		for(int i = 10; i >=0; i-- ) {
			System.out.print(i + " ");
		}
	}

}
